package com.monghit.java25.features;

/**
 * Demo de Simplified Main Methods
 *
 * La forma más simple de un método main en Java 25
 */
public class SimplifiedMainDemo {

    /**
     * Método main simplificado - sin modificadores de acceso
     * Perfecto para scripts simples y aprendizaje
     */
    void main() {
        System.out.println("¡Hola Mundo desde un main simplificado!");
        System.out.println("No necesita: public, static, String[] args");
    }
}
